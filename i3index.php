	<div class="pancho3"><div align="left"><p style="color: white; font-size:19px">Intel sigue ampliando la familia de procesadores de octava generación con la llegada del primer Core i3 para portátiles. Una versión que no goza del aumento de rendimiento de sus hermanos mayores duplicados en núcleos de procesamiento (Core i5 y Core i7), pero que aporta soluciones más económicas y de menor consumo.

El modelo anunciado es el Core i3-8130U y se entregará a los OEMs para la creación de portátiles básicos y ligeros, convertibles o 2 en 1. Frente a su predecesor (Core i3-7130U), rebaja ligeramente la frecuencia de trabajo base, pero aumenta significamente la velocidad de reloj en modo turbo. La memoria caché ha aumentado 1 MB y también la frecuencia de la memoria, hasta DDR4-2400.<br>

<br>


El apartado gráfico se mantiene sin cambios bajo la integrada 620 de Intel. Un chip con 12 líneas de PCI3 3.0, frecuencia de trabajo de hasta 1 GHz, soporte para DirectX 12 y OpenGL 4.4 y resoluciones máximas admitidas hasta 4096 x 2304 píxeles a 60 Hz bajo Display Port.<br>



Como verás, no ofrece tanta mejora respecto a la anterior generación como los Core i5 y Core 7, pero viene a cubrir el hueco entre ellos y los nuevos Pentium y Celeron, con equilibrio entre prestaciones y consumo.




